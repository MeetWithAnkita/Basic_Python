# 1. To determine the product and average of any two given numbers.

num01= eval(input("Enter the first number : "))
num02= eval(input("Enter the second number : "))

product = num01 * num02
average = (num01 + num02)/2

print(f"Result of product of {num01} and {num02} : {product} ")
print(f"Result of average of {num01} and {num02} : {average} ")