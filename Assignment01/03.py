# 3. To determine wage of workers for certain hours of work at a given
# hourly rate.
#  formula is 
# Wage=Hours Worked×Hourly Rate
working_hour = eval(input("Enter the working hour(Hour) :"))
hourly_rate = eval(input("Enter the hourly rate :"))
wage = working_hour * hourly_rate
print(f"The wage of workers is : {wage}")